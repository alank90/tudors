      <section id="popularity">
        <h2>Loss of popularity</h2>
        <p>
          While the greatness of England in Europe was being shown up as a sham,
          the regime was also losing popularity at home. The fanciful
          expectations of the early days could not, of course, endure; some
          measure of reality was bound to intrude. As it was, journalists and
          writers continued to be full of hope for a king who, from 1517,
          commanded the services of a new councillor, Sir Thomas More, one of
          the outstanding minds of the day. But More soon discovered that Henry
          found it easy to keep his enjoyment of learned conversation apart from
          the conduct of policy. Nothing for the moment could dent Wolsey’s
          strength, and this had serious drawbacks for the king, who supported
          him. The country was showing increasing signs of its discontent, and
          Wolsey’s efforts to remedy grievances only exasperated men of
          influence without bringing satisfaction to the poor. Feelings came to
          the boil in the years 1523–24. Although he disliked parliaments,
          Wolsey had to agree to the calling of one in 1523, but the taxes voted
          were well below what was required. Next year, the attempt to levy a
          special tax led to such fierce resistance that Henry rescinded it, he
          and the cardinal both trying to take the credit for the remission of
          what they had been jointly responsible for imposing. While he had
          Wolsey to take the blame, Henry could afford such fiascoes; the
          cardinal could not. By 1527 a government policy that, though seemingly
          Wolsey’s, was really the king’s was facing bankruptcy; ineffective
          abroad, unpopular at home, it made the regime look as empty of
          positive purpose as in fact it was.
        </p>
        <p>
          At this point, the king entered affairs unmistakably and
          spectacularly. Among his failures so far had been his or Catherine’s
          inability to provide a male heir to the throne; several stillbirths
          and early deaths had left only a girl, the princess Mary (born in
          1516), to carry on the line, and no one relished the thought of a
          female succession with all the dynastic and political uncertainties it
          would bring. Being the man he was, Henry could not suppose the fault
          to be his. His rapidly growing aversion to Catherine was augmented by
          his infatuation with one of the ladies of the court, Anne Boleyn, the
          sister of one of his earlier mistresses. Henry was no profligate;
          indeed, he had a strong streak of prudery, but he sought the
          occasional relief from marriage to a worthy but ailing wife to which
          princes have generally been held entitled. In Anne he met his match;
          this 20-year-old girl, brought up in a tough school of courtly
          intrigue, would be more than a king’s mistress. It took Henry, who in
          any case needed to marry her if the expected issue was to solve the
          succession problem, some six years to achieve their joint purpose.
          Inadvertently, he provoked a revolution.
        </p>
        <p>
          From 1527 Henry pursued what became known as “the King’s great
          matter”: his divorce from Catherine. He convinced himself that his
          first marriage had been against the divine law; that is, against the
          biblical injunction (Lev.) forbidding marriage with a brother’s widow.
          The deaths of the children proved God’s judgment on the union. With
          his characteristic readiness to convert his own desires into the law
          of God, Henry rapidly assured himself that he was living in mortal sin
          with Catherine and had to find relief if he was again to become
          acceptable to God. He appealed to Rome for a declaration of annulment.
          Popes had usually obliged kings in such matters, but Henry had picked
          both his time and his case badly. He was asking Pope Clement VII to
          help him discard the emperor’s aunt, but Clement, the emperor’s
          prisoner in 1527–28, never thereafter dared resist Charles, whose
          powerful feelings of familial honour and public prestige barred any
          concession to Henry’s wishes. Moreover, the pope’s reluctance was
          increased by the fact that he was being asked to declare illegal an
          earlier exercise of papal power—which had licensed Henry’s marriage to
          his brother’s widow—of a kind that brought a good deal of money to the
          papal coffers.
        </p>
        <p>
          Thus, Henry’s attempts to solve his dilemma in the accepted legal way
          were doomed from the start. Wolsey, in a worse dilemma, since only
          success in the impossible could keep him in power, obtained a trial of
          the case in England, but this was frustrated by his fellow judge,
          Cardinal Campeggio, on orders from Rome (1529). Within weeks, Wolsey
          was ousted, but his disappearance solved nothing, and the councillors
          who succeeded him could offer little help to their king, who knew only
          what he wanted, not how to get it.
        </p>
        <p>
          The chancellorship went to Thomas More, who had told Henry that he did
          not approve of the divorce and who wished to devote himself to a fight
          against Lutheran heresy. Confusion was the keynote of policy for some
          three years while the king dithered between hope that Rome might yet
          be forced to let the formal trial of his first marriage take place in
          England and stirrings of a more radical nature—to reject Rome
          outright. But, though he occasionally talked of doing just that,
          neither he nor anyone else knew how to convert talk into action.
        </p>
      </section>
      <section id="rome">
        <h2>The Breach with Rome</h2>
        <p>
          Action called for a revolution, and the revolution required a man who
          could conceive and execute it. That man was Thomas Cromwell, who, in
          April 1532, won control of the council and thereafter remained in
          command for some eight years. The revolution consisted of the decision
          that the English church should separate from Rome, becoming
          effectively a spiritual department of state under the rule of the king
          as God’s deputy on earth. The revolution that he had not intended gave
          the king his wish: in January 1533 he married Anne Boleyn; in May a
          new archbishop, Thomas Cranmer, presided over the formality of a trial
          that declared the first marriage annulled; in September the princess
          Elizabeth was born. The pope retaliated with a sentence of
          excommunication; it troubled no one.
        </p>
        <p>
          The supreme headship on earth over the Church of England, though he
          had not sought it, represented Henry’s major achievement. It had very
          wide-ranging consequences, but those that immediately concerned the
          king were two. In the first place, the new title consolidated his own
          concept of kingship, his conviction that (as he once said) he had no
          superior on earth. It rounded off the majestic image of divinely
          instituted royal rule that it was Henry’s constant ambition to present
          to an awed and obedient world. But, in the second place, it created a
          real personal problem for the king: earlier, in his book Assertio
          septem sacramentorum adversus Martinum Lutherum (1521), he had
          attacked Luther and had expressed a profound devotion to the papacy
          and had been rewarded with the title of Defender of the Faith. Now he
          had turned against the pope; his act was equal to encouraging the
          Protestant Reformation, a thing attractive to Cranmer and Cromwell
          (and perhaps Anne Boleyn) but not to Henry, who despised Luther. The
          religion of the newly independent church was for its head to settle:
          for the rest of his life, Henry, who prided himself on his theological
          learnings, was to give much time and thought to the nature of the true
          religion. With the exception of the papal primacy, he never gave up
          the main tenets of the faith in which he had grown up, but he changed
          his mind on details and arrived at an amalgam of his own in which
          transubstantiation and clerical celibacy mingled with radical views
          about the worldly authority of the church and man’s ability to seek
          salvation without the aid of priests.
        </p>
      </section>

      <section id="reforms">
        <h2>Domestic reforms</h2>
        <figure class="container-left">
          <img src="../assets/img/henry_at_40.jpg" alt="Henry VII at 40" />
          <figcaption>Henry at 40</figcaption>
        </figure>
        <p>
          Cromwell’s decade, the 1530s, was the only period of the reign during
          which a coherent body of policies was purposefully carried through.
          Cromwell’s work greatly enlarged Henry’s power, especially by
          transferring to the crown the wealth of the monasteries, dissolved in
          1536–40, and new clerical taxes; but it also, more explicitly than
          ever, subjected the king to the law and to the legislative supremacy
          of Parliament. Since Henry knew how to work with parliaments, the
          immediate effect was to make him appear more dominant than ever and to
          give to his reign a spurious air of autocracy—spurious because in fact
          the rule of law remained to control the sovereign’s mere will. The
          appearance of autocracy was misleadingly emphasized by the fact that
          all revolutions have their victims. As heads rolled, the king’s
          earlier reputation as a champion of light and learning was permanently
          buried under his enduring fame as a man of blood. Old friends such as
          More, refusing to accept the new order, fell before the onslaught, as
          did some 50 other men caught by the treason laws. Between 1538 and
          1541 the families of Pole and Courtenay were destroyed by the axe for
          treasons linked with efforts abroad to reverse the course of events in
          England but mainly because they could claim royal blood and
          represented a dynastic danger to the unprolific Tudor line.
        </p>
        <p>
          The king now embarked on the series of matrimonial adventures that
          made him appear both a monster and a laughingstock. He soon tired of
          Anne, who failed to produce a male heir; in 1536 she was executed,
          with other members of the court, for alleged treasonable adultery.
          Catherine of Aragon, rejected but unbowed, had died a little earlier.
          Henry immediately married Jane Seymour, who bore him his son Edward
          but died in childbirth (1537). The next three years were filled with
          attempts to replace her, and the bride chosen was Anne, sister of the
          duke of Cleves, a pawn in Cromwell’s policy for a northern European
          alliance against dangers from France and the Emperor. But Henry hated
          the first sight of her and at once demanded his freedom, an end
          achieved by a quick divorce.
        </p>
      </section>
      <section id="decline">
        <h2>Physical and mental decline</h2>
        <p>
          The Cleves fiasco destroyed Cromwell; it enabled his many enemies to
          turn the king against him, and in July 1540 his head fell on the
          scaffold. Henry had by now become truly dangerous: always secretive
          and suspicious, now he was beginning to show paranoiac tendencies.
          Convinced that he controlled everyone, he was in fact readily
          manipulated by those who knew how to feed his suspicions and pander to
          his self-righteousness. Full of experience—the oldest king in
          Europe—and increasingly competent in the routine of rule, he lacked
          the comprehensive vision and large spirit that would have made him a
          great man. His temperamental deficiencies were aggravated by what he
          regarded as his undeserved misfortunes and by ill health; he grew
          enormously fat. His mind did not weaken, but he grew restless,
          peevish, and totally unpredictable; often melancholy and depressed, he
          was usually out of sorts and always out of patience. In 1540–42 he
          briefly renewed his youth in marriage to the 20-year-old Catherine
          Howard, whose folly in continuing her promiscuity, even as queen,
          brought her to the block. The blow finished Henry. Thereafter, he was
          really a sad and bitter old man, and, though he married once more, to
          find a measure of peace with the calm and obedient Catherine Parr, his
          physical ruin was complete.

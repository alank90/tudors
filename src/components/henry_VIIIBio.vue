        <p>
          Henry himself displayed no military talent, but a real victory was won
          by the earl of Surrey at Flodden (1513) against a Scottish invasion.
          Despite the obvious pointlessness of the fighting, the appearance of
          success was popular. Moreover, in Thomas Wolsey, who organized his
          first campaign in France, Henry discovered his first outstanding
          minister. By 1515 Wolsey was archbishop of York, lord chancellor of
          England, and a cardinal of the church; more important, he was the
          king’s good friend, to whom was gladly left the active conduct of
          affairs. Henry never altogether abandoned the positive tasks of
          kingship and often interfered in business; though the world might
          think that England was ruled by the cardinal, the king himself knew
          that he possessed perfect control any time he cared to assert it, and
          Wolsey only rarely mistook the world’s opinion for the right one.
        </p>
        <p>
          Nevertheless, the years from 1515 to 1527 were marked by Wolsey’s
          ascendancy, and his initiatives set the scene. The cardinal had some
          occasional ambition for the papal tiara, and this Henry supported;
          Wolsey at Rome would have been a powerful card in English hands. In
          fact, there was never any chance of this happening, any more than
          there was of Henry’s election to the imperial crown, briefly mooted in
          1519 when the emperor Maximilian I died, to be succeeded by his
          grandson Charles V. That event altered the European situation. In
          Charles, the crowns of Spain, Burgundy (with the Netherlands), and
          Austria were united in an overwhelming complex of power that reduced
          all the dynasties of Europe, with the exception of France, to an
          inferior position. From 1521, Henry became an outpost of Charles V’s
          imperial power, which at Pavia (1525), for the moment, destroyed the
          rival power of France. Wolsey’s attempt to reverse alliances at this
          unpropitious moment brought reprisals against the vital English cloth
          trade with the Netherlands and lost the advantages that alliance with
          the victor of Pavia might have had. It provoked a serious reaction in
          England, and Henry concluded that Wolsey’s usefulness might be coming
          to an end.
        </p>

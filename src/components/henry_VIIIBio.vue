        <p class="intro">
          Henry VIII, (born June 28, 1491, Greenwich, near London, England—died
          January 28, 1547, London), king of England (1509–47) who presided over
          the beginnings of the English Renaissance and the English Reformation.
          His six wives were, successively, Catherine of Aragon (the mother of
          the future queen Mary I), Anne Boleyn (the mother of the future queen
          Elizabeth I), Jane Seymour (the mother of Henry’s successor, Edward
          VI), Anne of Cleves, Catherine Howard, and Catherine Parr.

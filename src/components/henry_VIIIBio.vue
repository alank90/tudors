      <section id="legacy">
        <h2>Legacy of Henry VIII</h2>
        <p>
          As king of England from 1509 to 1547, Henry VIII presided over the
          beginnings of the English Reformation, which was unleashed by his own
          matrimonial involvements, even though he never abandoned the
          fundamentals of the Roman Catholic faith. Though exceptionally well
          served by a succession of brilliant ministers, Henry turned upon them
          all; those he elevated, he invariably cast down again. He was
          attracted to humanist learning and was something of an intellectual
          himself, but he was responsible for the deaths of the outstanding
          English humanists of the day. Though six times married, he left a
          minor heir and a dangerously complicated succession problem. Of his
          six wives, two joined a large tally of eminent persons executed for
          alleged treason; yet otherwise his regime observed the law of the land
          with painful particularity. Formidable in appearance, in memory, and
          in mind, and fearsome of temper, he yet attracted genuine devotion and
          knew how to charm people. Monstrously egotistical and surrounded by
          adulation, he nevertheless kept a reasonable grasp on the possible;
          forever taking false steps in politics, he emerged essentially
          unbeaten and superficially successful in nearly everything he
          attempted to do.
        </p>

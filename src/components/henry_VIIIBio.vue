      <section id="accession">
        <h2>Accession to the Throne</h2>
        <p>
          Henry was the second son of Henry VII, first of the Tudor line, and
          Elizabeth, daughter of Edward IV, first king of the short-lived line
          of York. When his elder brother, Arthur, died in 1502, Henry became
          the heir to the throne; of all the Tudor monarchs, he alone spent his
          childhood in calm expectation of the crown, which helped give an
          assurance of majesty and righteousness to his willful, ebullient
          character. He excelled in book learning as well as in the physical
          exercises of an aristocratic society, and, when in 1509 he ascended
          the throne, great things were expected of him. Six feet tall,
          powerfully built, and a tireless athlete, huntsman, and dancer, he
          promised England the joys of spring after the long winter of Henry
          VII’s reign.
        </p>
        <p>
          Henry and his ministers exploited the dislike inspired by his father’s
          energetic pursuit of royal rights by sacrificing, without a thought,
          some of the unpopular institutions and some of the men that had served
          his predecessor. Yet the unpopular means for governing the realm soon
          reappeared because they were necessary. Soon after his accession,
          Henry married Catherine of Aragon, Arthur’s widow, and the attendant
          lavish entertainments ate into the modest royal reserves.
        </p>

        <p>
          In 1542 the emperor and the king of France resumed hostilities. After
          a pretense of independence, Henry again joined the former; the Scots
          promptly joined the French. The Scots were routed at Solway Moss
          (1542), and their king died soon after: this opened the possibility of
          subjugating that country permanently by means of a marriage alliance
          between the infant heirs to the two thrones. But the Scottish dream
          quickly collapsed as Henry’s crude handling of that nation gave
          control to a pro-French party, determined to resist even an alliance
          with England; physical conquest was beyond the king’s means. Henry
          personally managed both the war and the subsequent negotiations, and
          he displayed amazing energy for so sick a man. But energy is not the
          same thing as competence. The war proved ruinous. Money had to be
          raised by selling off the monastic lands, which had brought a good
          income; the desperate expedient of debasing the coinage, though it
          brought temporary succour, led to a violent inflation that made things
          worse. Yet, even after the emperor made peace with France (1544),
          Henry would not let go until two years later.
        </p>
        <p>
          As the year 1546 drew to a close, it was apparent to all observers
          that the king had not long to live. Not that it was clear to the man
          most concerned; he continued as before, lamenting religious
          dissension, attending to the business of government, continuing the
          pretense of deathless majesty, destroying the powerful Howard family,
          whom he suspected of plotting to control his successor. Conscious
          almost to the very end, he died on January 28, 1547. He left the realm
          feeling bereft and the government the more bewildered because, to the
          last, he had refused to make full arrangements for the rule of a boy
          king.
        </p>
